# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def isSymmetric(self, root: TreeNode) -> bool:
        if not root:
            return True

        def isSame(left, right):
            if not (left or right):
                return True
            if not (left and right):
                return False
            return left.val == right.val and isSame(left.left, right.right) and isSame(right.left, left.right)
        isSame(root.left, root.right)
